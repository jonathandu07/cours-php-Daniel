<?php
class Apprenant
{
    private $valeur;

    public function __construct()
    {
        $this->valeur;
    }
    public function destruct()
    {
    }

    public function setValeur($recuperation)
    {
        $this->valeur = $recuperation;
    }
    public function getValeur()
    {
        return $this->valeur;
    }
}
